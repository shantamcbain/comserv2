# Run this script from a machine with internet access to download all legacy ENCY .htm pages
# from the Internet Archive (Wayback Machine).
# Usage: bash download_from_wayback.sh
# Output goes to ./ency/ in the current directory
mkdir -p ency

download() {
    local ts="$1" orig="$2"
    local filename
    filename=$(basename "$orig" | sed 's|?.*||' | sed 's|:[0-9]*||')
    [ -z "$filename" ] && return
    local url="https://web.archive.org/web/${ts}if_/${orig}"
    echo "Downloading $filename ..."
    wget -q --timeout=30 --tries=3 -O "ency/$filename" "$url" \
      && echo "  OK: $filename" \
      || echo "  FAILED: $filename"
    sleep 0.5
}

